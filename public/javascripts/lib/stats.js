define(function () {
    return{
        populateData: function(){
            var self = this;
            $.getJSON( '/statsMAP?mode=tfidf', function( data ) {
                $('#idMAPTfidf').text("Current Mean Average Precision for the ranking generated by Tf-Idf: " + data);
            });
            $.getJSON( '/statsMAP?mode=llda', function( data ) {
                $('#idMAPLlda').text("Current Mean Average Precision for the ranking generated by L-LDA: " + data);
            });
            $.getJSON( '/statsAccuracy?mode=label_mode', function( data ) {
                $('#idAccuracyLM').text("Current Accuracy for the Label Mode: " + data["(tp.no + tn.no)/(tp.no + tn.no + fp.no + fn.no)"]);
            });
            $.getJSON( '/statsAccuracy?mode=term_mode', function( data ) {
                $('#idAccuracyTM').text("Current Accuracy for the Term Mode: " + data["(tp.no + tn.no)/(tp.no + tn.no + fp.no + fn.no)"]);
            });
        }
    }
});